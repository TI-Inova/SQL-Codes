SELECT ESA008_SUBGRUPO, TAA057_SUBGRUPO||'-'||TAA057_DESCRICAO 

 

FROM ESTBA008 

 

JOIN TATBA057 

  ON TAA057_GRUPO = ESA008_GRUPO 

 AND TAA057_EMPRESA = ESA008_EMPRESA 

 AND TAA057_AREA = ESA008_AREA 

 AND TAA057_FAMILIA = ESA008_FAMILIA 

 AND TAA057_GRUPOPROD = ESA008_GRUPOPROD 

 AND TAA057_SUBGRUPO = ESA008_SUBGRUPO 

 

WHERE ESA008_AREA = 2 

  AND ESA008_FAMILIA = :FAMILIA 

  AND ESA008_GRUPOPROD = :GRUPOPROD 

 

GROUP BY ESA008_SUBGRUPO, TAA057_SUBGRUPO||'-'||TAA057_DESCRICAO 