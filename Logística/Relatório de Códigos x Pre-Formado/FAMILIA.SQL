SELECT ESA008_FAMILIA, TAA100_FAMILIA||'-'||TAA100_DESCRICAO 

 

FROM ESTBA008 

 

JOIN TATBA100 

  ON TAA100_GRUPO = ESA008_GRUPO 

 AND TAA100_EMPRESA = ESA008_EMPRESA 

 AND TAA100_AREA = ESA008_AREA 

 AND TAA100_FAMILIA = ESA008_FAMILIA 

 

WHERE ESA008_AREA = 2 

 

GROUP BY ESA008_FAMILIA, TAA100_FAMILIA||'-'||TAA100_DESCRICAO 