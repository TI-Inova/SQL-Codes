SELECT ESA008_GRUPOPROD, TAA056_GRUPOPROD||'-'||TAA056_DESCRICAO 

 

FROM ESTBA008 

 

JOIN TATBA056 

  ON TAA056_GRUPO = ESA008_GRUPO 

 AND TAA056_EMPRESA = ESA008_EMPRESA 

 AND TAA056_AREA = ESA008_AREA 

 AND TAA056_FAMILIA = ESA008_FAMILIA 

 AND TAA056_GRUPOPROD = ESA008_GRUPOPROD 

 

WHERE ESA008_AREA = 2 

  AND ESA008_FAMILIA = :FAMILIA 

  

GROUP BY ESA008_GRUPOPROD, TAA056_GRUPOPROD||'-'||TAA056_DESCRICAO 