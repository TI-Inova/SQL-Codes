SELECT  TO_CHAR(RMTBE001.DATE_CREATED, 'DD/MM/RRRR HH24:MI') DATA, 

        RMC001_TIPODOCOP||'-'||RMC001_ORDEM ORDEM, 

         

        DECODE(RMB001_PRODUTOCONF, 0, RMB001_PRODUTO, RMB001_PRODUTO||'-'||RMB001_PRODUTOCONF) PRODUTO, 

        CQFBA019(RMB001_GRUPO, RMB001_EMPRESA, RMB001_PRODUTO, RMB001_PRODUTOCONF) PRODUTO_DESC, 

         

        OPS012_RECURSO||'-'||MPB001_DESCRICAO RECURSO, 

         

        DECODE(RMC001_PRODUTOCONF, 0, RMC001_PRODUTO, RMC001_PRODUTO||'-'||RMC001_PRODUTOCONF) COMPONENTE, 

        CQFBA019(RMC001_GRUPO, RMC001_EMPRESA, RMC001_PRODUTO, RMC001_PRODUTOCONF) COMPONENTE_DESC, 

         

        RMC001_UM UM, 

         

        RME001_QTDETRANSF QTD_TRANSF 

 

FROM RMTBE001 

 

JOIN RMTBB001 

  ON RMB001_GRUPO = RME001_GRUPO 

 AND RMB001_EMPRESA = RME001_EMPRESA 

 AND RMB001_FILIAL = RME001_FILIAL 

 AND RMB001_TIPODOC = RME001_TIPODOC 

 AND RMB001_RANCHO = RME001_RANCHO 

 AND RMB001_SOLICITACAO = RME001_SOLICITACAO 

 

JOIN RMTBC001 

  ON RMC001_GRUPO = RME001_GRUPO 

 AND RMC001_EMPRESA = RME001_EMPRESA 

 AND RMC001_FILIAL = RME001_FILIAL 

 AND RMC001_TIPODOC = RME001_TIPODOC 

 AND RMC001_RANCHO = RME001_RANCHO 

 AND RMC001_SOLICITACAO = RME001_SOLICITACAO 

 AND RMC001_SEQDEM = RME001_SEQDEM 

 

JOIN OPTBS012 

  ON OPS012_GRUPO = RME001_GRUPO 

 AND OPS012_EMPRESA = RME001_EMPRESA 

 AND OPS012_FILIAL = RME001_FILIAL 

  

 AND OPS012_TIPODOC = RMB001_TIPODOCOP 

 AND OPS012_ORDEM = RMB001_ORDEM 

 AND OPS012_LINHA = RMB001_LINHAOP 

 AND OPS012_PRODUTO = RMB001_PRODUTO 

 AND OPS012_PRODUTOCONF = RMB001_PRODUTOCONF 

  

JOIN MPTBB001 

  ON MPB001_GRUPO = OPS012_GRUPO 

 AND MPB001_EMPRESA = OPS012_EMPRESA 

 AND MPB001_RECURSO = OPS012_RECURSO 

 

JOIN CQTBA013 

  ON CQA013_USUARIO = RMTBE001.CREATED_BY 

 

JOIN OPTBS011 

  ON OPS011_ID = OPS012_OPS011_ID 

 

WHERE RMTBE001.DATE_CREATED BETWEEN :PERIODO AND :AAA 

 

  AND RME001_ORIGEM = 'TRANSFERENCIA_RANCHO' 

  AND CQA013_GRUPO = 13 

  AND OPS011_STATUS = 2 

 

ORDER BY ORDEM, COMPONENTE 