SELECT RME001_TIPODOC||'-'||RME001_RANCHO RANCHO,  

        RMC001_TIPODOCOP||'-'||RMC001_ORDEM ORDEM 

 

FROM RMTBE001, RMTBC001, CQTBA013 

 

WHERE RME001_GRUPO = RMC001_GRUPO 

  AND RME001_EMPRESA = RMC001_EMPRESA 

  AND RME001_FILIAL = RMC001_FILIAL 

  AND RME001_TIPODOC = RMC001_TIPODOC 

  AND RME001_RANCHO = RMC001_RANCHO 

  AND RME001_SOLICITACAO = RMC001_SOLICITACAO 

  AND RME001_SEQDEM = RMC001_SEQDEM 

 

  AND CQA013_USUARIO = RMTBE001.CREATED_BY 

 

  AND RMTBE001.DATE_CREATED BETWEEN :PERIODO AND :AAA 

 

  AND RME001_ORIGEM = 'TRANSFERENCIA_RANCHO' 

  AND CQA013_GRUPO = 13 

 

GROUP BY RME001_TIPODOC, RME001_RANCHO, RMC001_TIPODOCOP, RMC001_ORDEM 