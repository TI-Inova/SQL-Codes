SELECT TAA076_SUBGRUPO, TAA076_DESCRICAO 

 

FROM PPTBA004 

 

JOIN TATBA076  

  ON TAA076_GRUPO = PPA004_GRUPO 

 AND TAA076_EMPRESA = PPA004_EMPRESA 

 AND TAA076_AREA = PPA004_AREA 

 AND TAA076_SEGMENTO = PPA004_SEGMENTO 

 AND TAA076_GRUPOPESSOA = PPA004_GRUPOPESSOA 

 AND TAA076_SUBGRUPO = PPA004_SUBGRUPO 

 

WHERE PPA004_SEGMENTO NOT IN (21, 41, 51, 61, 71) 

   

  AND PPA004_GRUPOPESSOA = :SEGMENTO 

 

GROUP BY TAA076_SUBGRUPO, TAA076_DESCRICAO 