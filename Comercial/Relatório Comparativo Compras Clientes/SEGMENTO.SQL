SELECT TAA054_GRUPOPESSOA, TAA054_GRUPOPESSOA||' - '||TAA054_DESCRICAO 

 

FROM PPTBA004 

 

JOIN TATBA054  

  ON TAA054_GRUPO = PPA004_GRUPO 

 AND TAA054_EMPRESA = PPA004_EMPRESA 

 AND TAA054_AREA = PPA004_AREA 

 AND TAA054_SEGMENTO = PPA004_SEGMENTO 

 AND TAA054_GRUPOPESSOA = PPA004_GRUPOPESSOA 

 

WHERE PPA004_SEGMENTO NOT IN (21, 41, 51, 61, 71) 

 

GROUP BY TAA054_GRUPOPESSOA, TAA054_DESCRICAO 