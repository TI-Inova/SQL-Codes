SELECT SQL_1.CLIENTE,
       SQL_1.ITEM_GARANTIA,
       SQL_2.QTDE_ITEM

FROM (SELECT CASE 
                 WHEN ORC001_PESSOA IN (124, 175) THEN '124 - PROGAS / 175 - BRAESI'
                 ELSE TO_CHAR(ORC001_PESSOA ||' - '|| PPA001_NOME)
             END AS CLIENTE,
             SUM(1) AS ITEM_GARANTIA
       
FROM ORTBC001 

JOIN ORTBC010 ON ORC001_GRUPO = ORC010_GRUPO
             AND ORC001_EMPRESA = ORC010_EMPRESA
             AND ORC001_FILIAL = ORC010_FILIAL
             AND ORC001_TIPODOC = ORC010_TIPODOC
             AND ORC001_ORDEM = ORC010_ORDEM
             AND ORC001_ID = ORC010_ORC001_ID
             
JOIN PPTBA001 ON PPA001_GRUPO = ORC001_GRUPO
             AND PPA001_EMPRESA = ORC001_EMPRESA
             AND PPA001_PESSOA = ORC001_PESSOA
  
WHERE ORC010_GARANTIA = 'S'
  AND ORC001_PESSOA IN (124, 175, 226, 235, 240, 631)
  AND ORC001_DTFINALIZADO BETWEEN :DATAINI AND :DATAFIM
  
GROUP BY CASE 
             WHEN ORC001_PESSOA IN (124, 175) THEN '124 - PROGAS / 175 - BRAESI'
             ELSE TO_CHAR(ORC001_PESSOA ||' - '|| PPA001_NOME)
         END) SQL_1
         
JOIN (SELECT CASE 
           WHEN NSA001_CLIENTE  IN (124, 175) THEN '124 - PROGAS / 175 - BRAESI'
           ELSE TO_CHAR(NSA001_CLIENTE ||' - '|| PPA001_NOME)
       END AS CLIENTE, 
       SUM(NSB001_QTDE) AS QTDE_ITEM
       
FROM NSTBA001

JOIN NSTBB001 ON NSA001_GRUPO = NSB001_GRUPO
             AND NSA001_EMPRESA = NSB001_EMPRESA
             AND NSA001_FILIAL = NSB001_FILIAL
             AND NSA001_TIPODOC = NSB001_TIPODOC
             AND NSA001_NOTA = NSB001_NOTA
             AND NSA001_SERIE = NSB001_SERIE
             
JOIN PPTBA001 ON PPA001_GRUPO = NSA001_GRUPO
             AND PPA001_EMPRESA = NSA001_EMPRESA
             AND PPA001_PESSOA = NSA001_CLIENTE
             
WHERE NSA001_CLIENTE IN (124, 175, 226, 235, 240, 631)
  AND NSA001_DTEMISSAO BETWEEN :DATAINI AND :DATAFIM
  AND NSB001_NATUREZA IN (5101,6101)
  
GROUP BY CASE 
             WHEN NSA001_CLIENTE IN (124, 175) THEN '124 - PROGAS / 175 - BRAESI'
            ELSE TO_CHAR(NSA001_CLIENTE ||' - '|| PPA001_NOME)
       END) SQL_2 ON SQL_1.CLIENTE = SQL_2.CLIENTE;