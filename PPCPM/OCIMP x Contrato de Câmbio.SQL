SELECT IEA002_TIPODOCOC ORDEM_TIPODOC, 
        IEA002_ORDCOMPRA ORDEM, 
        IEA002_TIPODOC PROCESSO_TIPODOC, 
        IEA002_PROCESSO PROCESSO, 
        IEA002_SEQUENCIA SEQUENCIA, 
        CON002_TIPODOC CONTRATO_TIPODOC, 
        CON002_NROCONTRATO CONTRATO, 
        CON001_PESSOA PESSOA, 
        PPA001_NOME NOME 
 
FROM COTBN001, COTBN002, IETBA002, PPTBA001 
 
WHERE CON002_TIPODOCIE = IEA002_TIPODOC 
  AND CON002_PROCESSOIE = IEA002_PROCESSO 
  AND CON001_TIPODOC = CON002_TIPODOC 
  AND CON001_NROCONTRATO = CON002_NROCONTRATO 
  AND PPA001_PESSOA = CON001_PESSOA 
 
  AND IEA002_TIPODOCOC LIKE :TIPODOC 
  AND IEA002_ORDCOMPRA = :ORDEM  